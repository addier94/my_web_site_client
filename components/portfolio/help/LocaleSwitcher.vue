<template>
  <div class="locale-switcher">
    <select id="locale" v-model="$i18n.locale">
      <option v-for="locale in locales" :key="locale.key" :value="locale.key">
        {{ locale.label }}
      </option>
    </select>
    <label for="locale" aria-label="Locale">
      <fa style="color: black" :icon="['fas', 'globe']"></fa>
    </label>
  </div>
</template>

<script>
export default {
  data: () => ({
    locales: [
      { key: 'es', label: 'Espa√±ol' },
      { key: 'en', label: 'English' }
    ]
  }),
  watch: {
    '$i18n.locale': {
      handler(locale) {
        localStorage.setItem('locale', locale)
      }
    }
  }
}
</script>

<style lang="scss" scoped>
.locale-switcher {
  @apply absolute z-50;
  right: 2rem;
  label > svg {
    @apply text-primary_dark ml-2;
  }
}
